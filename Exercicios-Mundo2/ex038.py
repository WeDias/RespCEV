num1 = int(input('Digite Um Numero: '))
num2 = int(input('Digite Um Numero: '))
if num1 > num2:
    print('\n{} É Maior Que {}'.format(num1, num2))
elif num1 < num2:
    print('\n{} É Menor Que {}'.format(num1, num2))
elif num1 == num2:
    print('\n{} É Igual {}'.format(num1, num2))
